// 自动完成函数
export function getAutocompleteSuggestions(input: string, dataSource: string[]): string[] {
  if (!input) return []
  
  const lowercaseInput = input.toLowerCase()
  
  // 先匹配开头
  const startsWithMatches = dataSource.filter(item => 
    item.toLowerCase().startsWith(lowercaseInput)
  )
  
  // 再匹配包含
  const containsMatches = dataSource.filter(item => 
    !startsWithMatches.includes(item) && 
    item.toLowerCase().includes(lowercaseInput)
  )
  
  // 合并结果，最多返回10个建议
  return [...startsWithMatches, ...containsMatches].slice(0, 10)
} 